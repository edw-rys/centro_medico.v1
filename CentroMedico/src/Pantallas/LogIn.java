    private void activeButton(boolean b){
        txtPassword.setEnabled(b);
        txtUsuario.setEnabled(b);
        btnIniciar.setEnabled(b);
    }
    private void btnIniciarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIniciarMouseClicked
        //Iniciar sesion
        activeButton(false);
        String user=txtUsuario.getText();
        String clave=txtPassword.getText();
        if(user.equals("") && clave.equals("")){
            JOptionPane.showMessageDialog(null, "Rellene los campos");
            activeButton(true);
            return;
        }
        else if(user.equals("")){
            JOptionPane.showMessageDialog(null, "Digite usuario, por favor");
            activeButton(true);
            return;
        }
        else if(clave.equals("")){
            JOptionPane.showMessageDialog(null, "Digite clave, por favor");
            activeButton(true);
            return;
        }
        
        ConsultaBD consulta=new ConsultaBD();
        int rsp= consulta.consultarUsuarios(user, clave);
        if(rsp!=0){
            PantallaTrabajo iniciar=PantallaTrabajo.getInstance();
            iniciar.verificarUser(rsp);
            iniciar.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Datos erroneos!");
        }
        activeButton(true);
        
    }//GEN-LAST:event_btnIniciarMouseClicked

